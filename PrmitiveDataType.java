public class PrmitiveDataType {
    public static void main(String args[]){
        byte b=10;
        System.out.println("value of byte is " +b);
        short s=4;
        System.out.println("value of short is " +s);
        int i=2;
        System.out.println("value integer is " +i);
        long l=123456789;
        System.out.println("value of long is " +1);
        float f=11.21f;
        System.out.println("value of float is " +f);
        double d=12345.6789d;
        System.out.println("value of double is " +d);
        boolean B=true;
        System.out.println("value of boolean is " +B);
        char c='x';
        System.out.println("value of char is " +c);
    }
    
}
