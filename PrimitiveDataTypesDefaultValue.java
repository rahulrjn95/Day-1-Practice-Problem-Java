/* Write a Java program to display the Default Value of all Primitive Data Types
Types of Java.
 */
// Program to Print Primitive Data Types Default values
public class PrimitiveDataTypesDefaultValue {
    static boolean a;
    static double b;
    static float c;
    static int d;
    static long e;
    static String f;
    public static void main (String[]args){
        System.out.println("Default Values of Primitive Data Types:");
        System.out.println("Boolean = " + a);
        System.out.println("Double = " + b);
        System.out.println("Float = " + c);
        System.out.println("Integer = " + d);
        System.out.println("Long = " + e);
        System.out.println("String = " + f);

    }

}
